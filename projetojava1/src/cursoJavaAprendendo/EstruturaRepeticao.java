package cursoJavaAprendendo;

public class EstruturaRepeticao {

	public static void main(String[] args) {

		
		
		int numero  = 0;
		// O while vai executar enquanto a condição for verdadeira (true)
		while(numero <= 10){
			
			System.out.println("O numero atual é: " + numero);
			
			numero++;
			
		}
		
		/*-------------------------------------------------*/
		
		int numero2 = 0;
		do {
			System.out.println(numero2);
			numero2++;
			
		}while(numero2 <= 60);
		
		

	}

}
